float SemiLepD2StarWeights2D::calcBinError(int iVarBin) {
  // loop over all bins
  float fErrTotStat2 = 0.;
  float fErrTotSyst = 0.;
  for (int i = 0; i <= nMesons; i++) {
    int lclass = index2lclass[i];
    for (int iBinCosTh=0; iBinCosTh<nBinsCosTh[i]; iBinCosTh++) {
            for (int iBinW=0; iBinW<nBinsW[i]; iBinW++) {
	      int nEntries = (int)hEntries[iVarBin][i]->GetBinContent(iBinCosTh+1, iBinW+1);
	      float fErrStat = errStat(lclass, iBinW, iBinCosTh)*nEntries;
	      fErrTotStat2 += (fErrStat*fErrStat);
	      float fErrSyst = errSyst(lclass, iBinW, iBinCosTh)*nEntries;
	      fErrTotSyst += fErrSyst;
            }
    }
  }
  return TMath::Sqrt(fErrTotStat2 + fErrTotSyst*fErrTotSyst);
}

float SemiLepD2StarWeights2D::calcBinError(int iVarBin, bool isSyst) {
  // loop over all q2 bins
  float fErrTotStat2 = 0.;
  float fErrTotSyst = 0.;
  for (int i = 0; i <= nMesons; i++) {
    int lclass = index2lclass[i];
    for (int iBinCosTh=0; iBinCosTh<nBinsCosTh[i]; iBinCosTh++) {
      for (int iBinW=0; iBinW<nBinsW[i]; iBinW++) {
	int nEntries = (int)hEntries[iVarBin][i]->GetBinContent(iBinCosTh+1, iBinW+1);
	if (!isSyst) {
	  float fErrStat = errStat(lclass, iBinW, iBinCosTh)*nEntries;
	  fErrTotStat2 += (fErrStat*fErrStat);
	} else {
	  float fErrSyst = errSyst(lclass, iBinW, iBinCosTh)*nEntries;
	  fErrTotSyst += fErrSyst;
	}
      }
    }
  }
  return TMath::Sqrt(fErrTotStat2 + fErrTotSyst*fErrTotSyst);
}

void SemiLepD2StarWeights2D::reset() {
  for (int iVarBin=0; iVarBin<nVarBins; iVarBin++) {
    for (int i=0; i<nMesons; i++) {
      hEntries[iVarBin][i]->Reset();
    }
  }
}

void SemiLepD2StarWeights2D::setShape(int lclass, int iShape) {
  int idx = lclass2index[lclass];
  if(iShape < 0 ) {iPosWeight[idx] = iPosShape[idx][0]; return;}
  if(iShape - 1 > iPosShape[idx][2]-iPosShape[idx][1]) {iPosWeight[idx] = iPosShape[idx][0]; return;}
  iPosWeight[idx] = iPosShape[idx][0] + iShape;
}

void SemiLepD2StarWeights2D::resetShapes( void ) {
  for(int i =0; i<4; ++i) {
    iPosWeight[i] = iPosShape[i][0];
  }
}

